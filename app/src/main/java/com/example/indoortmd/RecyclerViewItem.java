package com.example.indoortmd;

public class RecyclerViewItem {
    private String id;
    private String mac;

    public void setId(String id) {
        this.id = id;
    }
    public void setMac(String mac) {
        this.mac = mac;
    }

    public String getId() {
        return id;
    }
    public String getMac() {
        return mac;
    }
}
